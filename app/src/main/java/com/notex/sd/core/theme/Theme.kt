data class NoteColors(
    val yellow: Color,
    val green: Color,
    val blue: Color,
    val pink: Color,
    val purple: Color,
    val orange: Color,
    val teal: Color,
    val gray: Color
)

val LightNoteColors = NoteColors(
    yellow = NoteYellow,
    green = NoteGreen,
    blue = NoteBlue,
    pink = NotePink,
    purple = NotePurple,
    orange = NoteOrange,
    teal = NoteTeal,
    gray = NoteGray
)

val DarkNoteColors = NoteColors(
    yellow = NoteYellowDark,
    green = NoteGreenDark,
    blue = NoteBlueDark,
    pink = NotePinkDark,
    purple = NotePurpleDark,
    orange = NoteOrangeDark,
    teal = NoteTealDark,
    gray = NoteGrayDark
)

val LocalNoteColors = staticCompositionLocalOf { LightNoteColors }

@Composable
fun NoteXTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val noteColors = if (darkTheme) DarkNoteColors else LightNoteColors

    CompositionLocalProvider(LocalNoteColors provides noteColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = NoteXTypography,
            content = content
        )
    }
}

object NoteXTheme {
    val noteColors: NoteColors
        @Composable
        get() = LocalNoteColors.current
}
